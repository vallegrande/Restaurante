package modelo;

public class Sucursal {
    private int codSucur;
    private String direcSucur;
    private String telfSucur;

    public int getCodSucur() {
        return codSucur;
    }

    public void setCodSucur(int codSucur) {
        this.codSucur = codSucur;
    }

    public String getDirecSucur() {
        return direcSucur;
    }

    public void setDirecSucur(String direcSucur) {
        this.direcSucur = direcSucur;
    }

    public String getTelfSucur() {
        return telfSucur;
    }

    public void setTelfSucur(String telfSucur) {
        this.telfSucur = telfSucur;
    }
    
}
